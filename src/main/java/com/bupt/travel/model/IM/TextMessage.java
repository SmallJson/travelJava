package com.bupt.travel.model.IM;

public class TextMessage {
    public String type;
    public String msg;
    public String getType() {
        return type;
    }
    public void setType(String type) {
        this.type = type;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
